function generateDefaultGallery(flower) {
    // Auto-generate a 3-image gallery based on the flower's main color
    const baseColor = flower.imageColor;
    return [
        { color: baseColor, emoji: flower.emoji, label: '–û–±—â–∏–π –≤–∏–¥' },
        { color: adjustGradient(baseColor, 'darker'), emoji: 'üíê', label: '–ö—Ä—É–ø–Ω—ã–π –ø–ª–∞–Ω' },
        { color: adjustGradient(baseColor, 'lighter'), emoji: flower.emoji, label: '–î–µ—Ç–∞–ª–∏' }
    ];
}

function adjustGradient(gradient, mode) {
    // Simple gradient adjustment for variety
    if (mode === 'darker') {
        return gradient.replace(/(\d+)%/g, (match, p1) => {
            const newPercent = Math.max(0, parseInt(p1) - 15);
            return newPercent + '%';
        });
    } else {
        return gradient.replace(/(\d+)%/g, (match, p1) => {
            const newPercent = Math.min(100, parseInt(p1) + 15);
            return newPercent + '%';
        });
    }
}

